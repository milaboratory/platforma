#!/usr/bin/env node

import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";
import { parseDocument } from "yaml";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const workspacePath = path.resolve(__dirname, "../../../pnpm-workspace.yaml");
const outPath = path.resolve(__dirname, "../src/pinned-versions.generated.ts");

const PINNED_PACKAGES = ["ag-grid-enterprise", "ag-grid-vue3"];

const content = fs.readFileSync(workspacePath, "utf8");
const doc = parseDocument(content);
const catalog = doc.toJSON()?.catalog;

if (!catalog) {
  throw new Error("no 'catalog' section in pnpm-workspace.yaml");
}

const missing = PINNED_PACKAGES.filter((pkg) => typeof catalog[pkg] !== "string");
if (missing.length > 0) {
  throw new Error(
    `pinned packages not found in pnpm-workspace.yaml catalog: ${missing.join(", ")}`,
  );
}

const entries = PINNED_PACKAGES.map(
  (pkg) => `  ${JSON.stringify(pkg)}: ${JSON.stringify(catalog[pkg])},`,
);

const generated = `// Auto-generated by scripts/generate-pinned.mjs — do not edit
export const PINNED_VERSIONS: Record<string, string> = {
${entries.join("\n")}
};
`;

fs.writeFileSync(outPath, generated, "utf8");
console.log(`generated ${path.relative(process.cwd(), outPath)}`);
